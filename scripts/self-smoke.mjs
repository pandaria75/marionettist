async function getExpectedMirrorFiles() {
  const relativeFiles = [];
  for (const root of ["agents", "commands"]) {
    await collectRelativeFiles(path.join(repoRoot, "templates", "pathways", "opencode", root), `.opencode/${root}`, relativeFiles);
  }
  relativeFiles.sort();
  return relativeFiles;
}

async function collectRelativeFiles(sourceDirectory, targetPrefix, relativeFiles) {
  const children = await fs.readdir(sourceDirectory, { withFileTypes: true });
  for (const child of children) {
    const childSource = path.join(sourceDirectory, child.name);
    const childTarget = `${targetPrefix}/${child.name}`;
    if (child.isDirectory()) {
      await collectRelativeFiles(childSource, childTarget, relativeFiles);
      continue;
    }
    if (child.isFile()) {
      relativeFiles.push(childTarget);
    }
  }
}

async function readExistingFiles(relativeFiles) {
  const entries = [];
  for (const relative of relativeFiles) {
    const absolute = path.join(repoRoot, relative);
    entries.push([relative, await pathExists(absolute) ? await fs.readFile(absolute, "utf8") : null]);
  }
  return entries;
}

async function readRelative(relative) {
  return fs.readFile(path.join(repoRoot, relative), "utf8");
}

async function resolvedTemplateOpencodePath(sourceRelative) {
  const resolvedSource = await resolveOpencodeTemplateSource(sourceRelative);
  assert(resolvedSource, `expected resolved template source for ${sourceRelative}`);
  return resolvedSource.sourceRelative;
}

async function snapshotDirectory(directoryPath) {
  const entries = [];
  await collectFiles(directoryPath, "", entries);
  return entries;
}

async function collectFiles(directoryPath, relativePrefix, entries) {
  const children = await fs.readdir(directoryPath, { withFileTypes: true });
  for (const child of children) {
    const childAbsolute = path.join(directoryPath, child.name);
    const childRelative = relativePrefix ? `${relativePrefix}/${child.name}` : child.name;
    if (child.isDirectory()) {
      await collectFiles(childAbsolute, childRelative, entries);
      continue;
    }
    entries.push([childRelative, await fs.readFile(childAbsolute, "utf8")]);
  }
  entries.sort(([left], [right]) => left.localeCompare(right));
  return entries;
}

async function pathExists(filePath) {
  try {
    await fs.access(filePath);
    return true;
  } catch {
    return false;
  }
}

async function readProjectManifest(projectPath) {
  return JSON.parse(await fs.readFile(path.join(projectPath, ".marionettist", "manifest.json"), "utf8"));
}

async function switchOrdinaryProjectToLocalFallback(projectPath, { commandSurface = "minimal" } = {}) {
  const configPath = path.join(projectPath, "marionettist.config.yaml");
  let config = await fs.readFile(configPath, "utf8");
  config = config.replace('pluginSource: "package"', 'pluginSource: "local"');
  await fs.writeFile(configPath, config, "utf8");

  const syncArgs = ["sync", "--project", projectPath, "--with-opencode"];
  if (commandSurface === "full") {
    syncArgs.push("--opencode-command-surface", "full");
  }

  return harness(...syncArgs);
}

function assert(condition, message) {
  if (!condition) {
    throw new Error(message);
  }
}

function assertIncludes(content, expected) {
  assert(content.includes(expected), `Expected output to include: ${expected}\nActual output:\n${content}`);
}

function assertSameFileSnapshot(left, right, message) {
  assert(JSON.stringify(left) === JSON.stringify(right), message);
}

function assertExcludes(content, unexpected) {
  assert(!content.includes(unexpected), `Expected output to exclude: ${unexpected}\nActual output:\n${content}`);
}
